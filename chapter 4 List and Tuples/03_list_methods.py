#sorting
l1=[1,8,7,2,21,15]
l1.sort()#sorts the list ascending order
print(l1)
#reversing the list
l1.reverse()#reverses the sequence of the number
print(l1)
#appending the list
l1.append(8)#appends the values at the end of the list
print(l1)
#inserting a number in the list
l1.insert(0,544)
print(l1)
#deleting at certain index
l1.pop(2)# removes element at index 2
print(l1)
#deleting certain element
l1.remove(15)#removes 15 from the list
print(l1)
