#using 'int' to convert any data type into integer
a="3534"
a= int(a)#it will try to convert any data type into integer
print(type(a))
print(a+5)
#using 'float' to convert any data type into float
a="3534"
a= float(a)#it will try to convert any data type into float
print(type(a))
print(a+5)
